public class WikiPersonaje {

  private String clase,vida,atq,def,agi;
  private String detalle,img_pic,img_view;

    public WikiPersonaje(){}

    public void setClase(String clase) {
        this.clase = clase;
    }

    public String getClase() {
        return clase;
    }

    public void setVida(String vida) {
        this.vida = vida;
    }

    public String getVida(){
        return vida;
    }

    public void setAtq(String atq) {
        this.atq = atq;
    }

    public String getAtq(){
        return atq;
    }

    public void setDef(String def) {
        this.def = def;
    }

    public String getDef(){
        return def;
    }

    public void setAgi(String agi) {
        this.agi = agi;
    }

    public String getAgi(){
        return agi;
    }

    public void setDetalle(String detalle) {
        this.detalle = detalle;
    }

    public String getDetalle(){
        return detalle;
    }

    public void setImg_pic(String img_pic) {
        this.img_pic = img_pic;
    }

    public String getImg_pic(){
        return img_pic;
    }

    public void setImg_view(String img_view) {
        this.img_view = img_view;
    }

    public String getImg_view(){
        return img_view;
    }
}
